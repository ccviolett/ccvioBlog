<template><div><h1 id="概述" tabindex="-1"><a class="header-anchor" href="#概述" aria-hidden="true">#</a> 概述</h1>
<p>机械设计是一门全面讲解如何设计一个机械设备的课程。</p>
<p>在从零开始设计一个设备之前，在这门课的最开始，我们首先要知道如何去评估一个设备，我们需要一些基础的理论知识来辅助我们后面的判断。</p>
<p>对于一个零件，我们需要知道整个设备不运行或运行的时候，他上面会受到什么力，这个零件能否承载这样的力，能不能让整个设备正确运行，这就是第三章——机械零件的强度。</p>
<p>整个设备在动起来之后，两个零件之间，或者零件的内部，肯定会存在力的相互作用，如果存在相对的运动趋势的话，就会产生摩擦，我们需要知道计算的方式和减小摩擦的方法，以让整个系统不至于在运行的过程中突然出现故障报废。于是我们就需要学习第四章——摩擦、磨损、润滑的概念。</p>
<p>在有了基础知识后，我们开始我们的设计之旅，首先考虑静态的一个设备，这个设备不可能是一个整体，而是由很多个零件组成。那么多的零件，肯定会需要一些东西来将他们固定在一起，这就是第二篇——连接。</p>
<p>常见的连接方式有三种：</p>
<p>第五章——螺纹连接和螺旋传动，我们生活中拼个桌子、椅子，用到的螺丝钉，就是这种连接方式。</p>
<p>第六章——键、花键、无键连接和销连接，这个稍微少见一点，不过在电影、电视剧中，我们大概率见过那种木工师傅在修理的时候，如果有缝隙，可能会拿那种方形或者楔形的木块往里面敲，那个就是一种销连接。</p>
<p>第七章——铆接、焊接、胶接和过盈连接，如果想把两块铁皮接在一起，那么薄的两片，其他的办法可能很难完成，这时候就可以高温融化焊丝，把他们焊接在一起，这个在生活中、在工厂里我们也经常见到。</p>
<p>在知道了基本的连接方式之后，我们结合各种方法，已经具备了做一个静态的模型出来的能力，可是机械设计肯定不是为了设计一个大铁块，肯定是想要设计一个能够有用的东西，让我们的机械动起来。于是我们来到了第三篇——机械传动。</p>
<p>机械传动常见的方式有四种：</p>
<p>第八章——带传动，进安检时候放包、放箱子的机器，这是我们生活中可能见得最多的带传动装置。在电视剧或者电影之中，我们可能也见过那种大块头的柴油发动机，上面一般会有一个大大的皮带，这个也是带传动。两个轮子一个皮带，就可以完成我们的带传动。</p>
<p>第九章——链传动，在我们生活中，单车就是最常见的链传动，单车的链条卡好在前后的两个链轮上，这样我们踩脚踏，力就可以在两个轮子之间进行传动。</p>
<p>第十章——齿轮传动，可是说是最常用的传动了，也是我们印象中机械的代名词，两个巨大的齿轮从静止开始啮合，然后缓慢旋转，然后一个机关被破解开，神秘的门缓缓升起，这个是很多电视剧、电影、游戏中尝尝出现的画面，这里面所用到的就是齿轮传动。我们在后面要重点学习的减速器中，也是用到的齿轮传动。</p>
<p>第十一章——蜗杆传动，这个在我们生活中见得不多，因为蜗杆传动一般只用在重型机械的传动上。蜗杆传动也尝用于减速器的设计中。</p>
<p>每种传动都有其特点，就像不同的连接一样，我们应该在不同场合灵活运用不同的传动。</p>
<p>至此，我们已经知道如何连接零件，并且让这个设备动起来，将力传动到我们需要的地方。理论上我们已经可以脑洞大开开始设计，但是实际上我们发现我们并不知道如何去设计，此时我们就需要学习一些经典的机械设计套路，一些成熟的机械设计方案。</p>
<p>最重要的机械设计方案莫过于轴系设计，也就是我们的第四篇——轴系零、部件。</p>
<p>所谓轴系设计，就是一根特定形状的棍子连接上了齿轮、蜗杆等用于传动的零件，然后通过一系列内部摩擦力很小的零件把滚子架在一个架子上，然后若干个这样特定的棍子之间连在一起就可以传递力，可以让设备进行特定的运动。</p>
<p>用更加专业的语言来描述，这里面”内部摩擦力很小的零件“就是第十二章——滑动轴承和第十三章——滚动轴承，把这些“棍子“连在一起或者按照我们的想法让这些棍子分离的就是第十四章——联轴器和离合器，我们一直所说的这个”棍子“就是第十五章——轴。</p>
<p>在学完这些后，我们已经能够运用这些成熟的方案去解决机械设计的难题了，只剩下一切边角需要补充的知识，于是我们来到第五篇——其他零、部件。</p>
<p>这里面包含了我们生活中常见，在机械设计中也有一定用处的，可以提供缓冲、提供拉力等作用的第十六章——弹簧，用于支承整个机械设备、包装设备的第十七章——机座和箱体简介，以及一个最常见的机械设计模型第十八章——减速器和变速器。</p>
<p>这就是整本”机械设计“教材所带给我们的东西，从最开始的零部件指标计算，到零部件的静态连接，到动态的机械传动，随后学习了轴系的设计，最后了解了一些其他的零部件。在学习完成所有的这些知识后，我们就有能力自己动手去进行机械设计了。</p>
